    <p>Runs, swims, walks, biking and other types of physical activity. The details we get include the start time,
        duration, pace, calories, etc. If available, we also load the latitude/longitude coordinates to plot it on a
        map. We also load your heart rate information if available.</p>
    <h3>Withings/Nokia</h3>
    <p>We sync with Withings to retreive bodyweight measurements taken with their wireless scales. We also load blood
        pressure and heart rates. If you use the Withings sleep tracker, we also load your sleep data.</p>
    <h3>Moves, Foursquare</h3>
    <p>We load a list of the places you've been to. From Moves, these contain Foursquare or Facebook locations and the
        start and end times. From Foursquare, these consist of checkins at a point in time. These can contain
        latitude/longitude coordinates and addresses of locations. If both Moves and Foursquare are connected at the
        same time, we prioritize the data from Moves because it has more details about the time spent—for example that
        you were at a restaurant for an hour and a half, rather than just that you were there at a specific time of day.
        From Foursquare, we may also load photos that were associated with your checkins.</p>
    <h3>RescueTime</h3>
    <p>We use RescueTime to calculate your productivity for each day and show your top apps and websites. We load an
        hourly list of what apps and websites you used. Pages visited in incognito mode are not tracked by RescueTime
        and not loaded by us. We understand that this data is potentially very sensitive, and only show basic averages
        of productivity and a few of the top apps, and not individual websites or applications.</p>
    <h3>Twitter</h3>
    <p>We connect to Twitter to load your latest shared photos. We look at the favorite counts to feature the top photos
        you've shared on Twitter. We get read-only access and never will write to your Twitter—we only use it to get
        your latest shared content.</p>
    <h3>Google Fit</h3>
    <p>Especially for users on the Android platform, we use Google Fit to load stats like step counts and heart
