    <h3>The Vault</h3>
    <p>A Pro feature called the Vault gives you additional granularity over what gets shared. For example, you can add
        friends but move your locations or weight to the Vault if you don’t want them to be able to see it. Otherwise by
        default, adding friends will let them see the same view of your profile as yourself.</p>
