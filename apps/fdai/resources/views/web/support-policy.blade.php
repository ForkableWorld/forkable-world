                <strong>SEVERITY DEFINITIONS</strong>

                Please refer to your API Product Terms of Service for any additional API product specific details.

                <strong>Critical (Severity 1)</strong>

                Critical production issue affecting all users, including system unavailability and data integrity issues with no workaround available. This may include one or more of the following:
                <ul>
                    <li>Service is down or unavailable.</li>
                    <li>A critical part of the API infrastructure is unavailable or inaccessible, resulting in total disruption of work or critical business impact.</li>
                    <li>Service crashes or hangs indefinitely causing unacceptable or indefinite delays for resources or response.</li>
                    <li>Data corrupted or lost and must restore from backup.</li>
                    <li>A critical documented feature / function is not available.</li>
                </ul>
                Severity 1 issues identified by the customer but not related to a service interruption/outage require the customer to have dedicated resources available to work on the issue on an ongoing basis.

                <strong>Major (Severity 2)</strong>

                Major functionality is impacted or significant performance degradation is experienced. The issue is persistent and affects many users and/or major functionality. No reasonable workaround is available. This may include one or more of the following:
                <ul>
                    <li>Service is operational but highly degraded performance to the point of major impact on usage.</li>
                    <li>Important features of the API are unavailable with no acceptable workaround; however, operations can continue in a restricted fashion.</li>
                    <li>Access to a particular third-party application or service provider deemed noncritical is impacted</li>
                </ul>
                <strong>Minor (Severity 3)</strong>

                System performance issue or bug affecting some but not all users. Short-term workaround is available, but not scalable. This may include one or more of the following:
                <ul>
                    <li>Service is operational but partially degraded for some or all customers, and an acceptable workaround or solution exists.</li>
                    <li>Problem with non-critical feature or functionality</li>
                </ul>
                <strong>Cosmetic (Severity 4)</strong>

                An inquiry regarding a routine technical issue; information requested on application capabilities, navigation, installation or configuration; bug affecting a small number of users. An acceptable workaround is available. This may include one or more of the following:
                <ul>
                    <li>Minor problem not impacting service functionality.</li>
                    <li>Enhancement requests, missing or erroneous documentation.</li>
                    <li>Minor problem or question that does not affect the delivery of service.</li>
                </ul>
                <strong>SUPPORT REQUEST SEVERITY LIFE CYCLE</strong>

                Support Requests are assigned a severity level based on the nature of your request. We highly recommend you familiarize yourself with the Severity definitions and response times defined above.

                <strong>Severity 1 Problem Reporting</strong> — Please first check the QuantiModo API Status Page to see if the outage has already been reported or if your issue is due to a scheduled API product maintenance. If you do not see your issues reported, please report service outages/severity 1 problems via email.

                <strong>Downgrade of Severity Level</strong> — If, during the Support Request process, the issue no longer warrants the severity level currently assigned based on its current impact on the production operation of the API product, then the severity level will be downgraded to the severity level that most appropriately reflects its current impact.

                <strong>Upgrade of Severity Levels</strong> — If, during the Support Request process, the issue warrants the assignment of a higher severity level than that currently assigned based on the current impact on the production operation of the API product, then the severity level will be upgraded to the severity level that most appropriately reflects its current impact. In requesting an assignment of a higher severity level, you must provide QuantiModo with sufficient information that demonstrates the increased impact of the issue on the production operation of the service.

                <strong>Adherence to Severity Level Definitions</strong> — You shall ensure that the assignment and adjustment of any severity level designation is accurate based on the current impact on the production operation of the API product. You acknowledge that QuantiModo is not responsible for any failure to meet performance standards caused by your misuse or mis-assignment of severity level designations.

                <strong>SUPPORT REQUEST ESCALATION</strong>

                At QuantiModo, our support team always works to ensure that the appropriate resources and level of focus are applied to your request to ensure a timely resolution. If you are not satisfied with the progress of your Support Request, however, you can request an escalation. By invoking the escalation process, additional levels of QuantiModo management attention, procedure reinforcement, and resource prioritization are brought to bear on resolving your Support Request.

