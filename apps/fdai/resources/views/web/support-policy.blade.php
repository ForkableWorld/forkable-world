                These QuantiModo Application Programming Interface (“API”) Support Policies apply to the support provided by QuantiModo, Inc. (“QuantiModo”), as part of a QuantiModo API license.

                These API Support Policies are subject to change at QuantiModo’s discretion.

                <strong>API UPDATES</strong>

                “API Update” means a subsequent release of the API product, which QuantiModo generally makes available for its API customers with an active API license and support contract at no additional fee. API Updates may contain but are not limited to security fixes, critical patch updates, general maintenance releases, selected functionality, and documentation updates.

                QuantiModo will provide API Updates when available and at its discretion. QuantiModo is under no obligation to develop any future functionality or enhancements. If an update for an API product is made available to you pursuant to these API Support Policies, it shall automatically replace the previous version of the applicable API product. QuantiModo will continue to support previous major versions of the API for a period of 12 months from the time an updated version is made available.

                Where practical, QuantiModo will schedule such API Updates during non-business hours and will provide customers with advance notice via email or the QuantiModo API Status Page.

                <strong>DEDICATED INTERNAL HELP DESK</strong>

                You are expected to establish and maintain an internal help desk for your customers and act as first-line support for your product within which the QuantiModo API product is incorporated. First-line support shall include, but not be limited to: (i) a direct response to users with respect to inquiries concerning the performance, functionality or operation of the API product, (ii) a direct response to users with respect to problems or issues with the API product, (iii) a diagnosis of problems or issues of the API product, and (iv) a resolution of known problems or issues with the API product with the help of technical knowledge base articles, repositories and experience.

                If after reasonable efforts you are unable to diagnose or resolve the API product problems or issues, you may contact QuantiModo for technical support by emailing us at support@quantimo.do.

                QuantiModo Technical Support consists of (i) a diagnosis of problems or issues with the API product, and (ii) reasonable efforts to resolve verifiable reported errors in the API product so that it performs materially as described in the online API product documentation.

                QuantiModo may review Support Requests logged by your technical contacts and may recommend specific organization and process changes to assist you with the practices described in these API Support Policies.

                <strong>TECHNICAL LEADS</strong>

                Your Technical and/or Project Leads are the liaisons between you and QuantiModo for API Technical Support. These persons must be knowledgeable about the QuantiModo API product you have licensed and your technical environment in order to work with QuantiModo to analyze and resolve Support Requests. They are responsible for engaging QuantiModo technical support and monitoring the resolution of all Support Requests and escalated support issues.

                Your Technical Leads assigned to monitor and administer your QuantiModo API product should have experience in network troubleshooting as well as browser knowledge &amp; debugging skills. In addition, they should have experience troubleshooting third-party web application integration.

                Technical Leads are responsible for checking QuantiModo’s online resources (e.g. website technical documentation and knowledge base solutions) and the QuantiModo API Status Page, if available before submitting a Support Request as the issue or problem may have already been reported. Status of service outages and API product maintenance schedule is updated on the QuantiModo API Status Page.

                <strong>RESPONSE TIMES</strong>

                QuantiModo is committed to the rapid response of all Support Requests. All severities can be logged with QuantiModo on a 24 hours-per-day, 7 days-per-week, 365 days-per-year basis via email at support@quantimo.do.

                QuantiModo will use commercially reasonable efforts to promptly respond to each Support Request. QuantiModo does not guarantee resolution time as that may vary on the nature of the problem. A resolution may consist of a fix, workaround or other solution QuantiModo deems reasonable.

                QuantiModo will use reasonable efforts to meet the Service Level Objectives stated in the table below. QuantiModo will provide continuous efforts (24x7x365) to resolve Severity 1 API service availability issues until a workaround or resolution can be provided or until the incident can be downgraded to a lower severity.

